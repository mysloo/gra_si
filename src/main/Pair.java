package main;

public class Pair {
    private double x, y;

    public Pair(double xx, double yy){
        x = xx;
        y = yy;
    }
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
